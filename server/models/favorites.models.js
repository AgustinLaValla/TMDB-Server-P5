import mongoose from "mongoose";

const favoriteSchema = new mongoose.Schema(
  {
    movieId: {
      type: Number,
      required: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("Favorites", favoriteSchema);
